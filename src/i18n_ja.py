def merge_japanese_text(text_map: dict, *, app_title_text: str) -> dict:
    """Copy text_map and attach ja locale with runtime title."""
    out = {lang: dict(values) for lang, values in text_map.items()}
    ja = dict(TEXT_JA)
    ja["title"] = app_title_text
    out["ja"] = ja
    return out
